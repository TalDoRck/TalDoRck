let nt1, nt2, nt3, nt4, media

nt1 = Number(prompt("Informe a primeira nota: "))
nt2 = Number(prompt("Informe a segunda nota: "))
nt3 = Number(prompt("Informe a terceira nota: "))
nt4 = Number(prompt("Informe a quarta nota: "))

media = parseInt(nt1 + nt2 + nt3 + nt4) / 4

alert("Sua média é " + media)
