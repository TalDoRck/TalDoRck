let n1, n2, p1, p2, mediaPonderada

n1 = Number(prompt("Informe a primeira nota: "))
p1 = Number(prompt("Informe a segunda nota: "))
n2 = Number(prompt("Informe a terceira nota: "))
p2 = Number(prompt("Informe a quarta nota: "))

mediaPonderada = parseInt(nt1 * p1 + n2 * p2) / (p1 + p2)

alert("Sua média ponderada é " + mediaPonderada)
